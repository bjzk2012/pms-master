package cn.kcyf.pms.core.enumerate;

public enum QuestionCause {
    CODEERROR("代码错误"),
    CONFIG("配置相关"),
    INSTALL("安装部署"),
    SECURITY("安全相关"),
    PERFORMANCE("性能问题"),
    STANDARD("标准规范"),
    AUTOMATION("测试脚本"),
    DESIGNDEFECT("设计缺陷"),
    OTHERS("其他");

    QuestionCause(String message) {
        this.message = message;
    }

    private String message;

    public String getMessage() {
        return message;
    }
}
