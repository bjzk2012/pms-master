package cn.kcyf.pms.core.enumerate;

public enum QuestionCategory {
    BUSINESS("业务咨询"),
    DESKTOP_SUPPORT("桌面支持"),
    SPECIALTY_SUPPORT("技术支持"),
    DEVICE_OPERATE("设备操作"),
    REQUIREMENTS_CHANGE("需求变更"),
    BUG("系统缺陷"),
    OTHERS("其他");
    QuestionCategory(String message) {
        this.message = message;
    }

    private String message;

    public String getMessage() {
        return message;
    }
}
