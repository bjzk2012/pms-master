package cn.kcyf.pms.core.enumerate;

public enum QuestionStatus {
    DRAFT("草稿"),
    ACTIVE("激活"),
    CLOSED("已关闭"),
    SOLVED("已解决");

    QuestionStatus(String message) {
        this.message = message;
    }

    private String message;

    public String getMessage() {
        return message;
    }
}
